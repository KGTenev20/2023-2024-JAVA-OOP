public class Main {
    public static void main(int num1, int num2) {
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        if(num2 != 0)
        {
            System.out.println(num1 / num2);
        }

        if(num2 != 0)
        {
            System.out.println(num1 % num2);
        }
    }
}